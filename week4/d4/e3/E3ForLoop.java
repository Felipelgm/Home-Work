package week4.d4.e3;

public class E3ForLoop {
    public static void main(String[] args) {

        // Objective: Print "Hey, it is year:" followed by the years from 2025 to 2030 (inclusive).

        // Step 1: Initialize the loop variable 'year' to 2025.

        // Step 2: Set the loop condition so that the loop continues as long as 'year' is less than or equal to 2030.

        // Step 3: Inside the loop, first print the string "Hey, it is year:".

        // Step 4: Then, print the current value of the 'year' variable.

        for (int year=2025; year<=2030;year++){
            System.out.println("Hey, it is year:");
            System.out.println(year);

        }


        for (int year=2025; year<=2030; year++) {
            System.out.println("Hey, it is year: " +year);


        }


    }
}
